const queryDocuments = async (query, topK = 5) => {
    try {
        // Generar embedding para la consulta
        const queryEmbeddingResponse = await openai.embeddings.create({
            model: "text-embedding-3-small",
            input: query,
            encoding_format: "float"
        });
        const queryEmbedding = queryEmbeddingResponse.data[0].embedding;

        // Consultar Pinecone para encontrar documentos similares
        const index = await initializePinecone();
        const queryResponse = await index.query({
            queryRequest: {
                vector: queryEmbedding, // Embedding generado para la pregunta
                topK, // N√∫mero de resultados a recuperar
                includeMetadata: true // Esto asegura que tambi√©n se obtiene el metadata, que incluye el texto
            }
        });

        // Extraer el texto de los documentos m√°s relevantes
        const contexts = queryResponse.matches.map(match => match.metadata.text);

        // Calcular la puntuaci√≥n promedio de relevancia
        const averageScore = queryResponse.matches.reduce((acc, match) => acc + match.score, 0) / queryResponse.matches.length;

        // Construir el prompt para OpenAI
        const prompt = `
        Contexto: ${contexts.join('\n\n')}
         Pregunta: ${query} Por favor, responde a la pregunta bas√°ndote √∫nicamente en el contexto proporcionado. Si la informaci√≥n no est√° disponible en el contexto, indica que no puedes responder con la informaci√≥n disponible.
         `;

        // Generar respuesta con ChatGPT
        const completion = await openai.chat.completions.create({
            model: "gpt-4o-mini",
            messages: [{
                role: "system",
                content: "Eres un asistente experto que responde preguntas bas√°ndose √∫nicamente en el contexto proporcionado."
            },
            {
                role: "user",
                content: prompt
            }],
            max_tokens: 500
        });

        const answer = completion.choices[0].message.content;

        return {
            answer,
            relevanceScore: averageScore,
            contexts: queryResponse.matches.map(match => ({
                score: match.score,
                text: match.metadata.text,
                filename: match.metadata.filename
            }))
        };

    } catch (error) {
        console.error("Error al consultar documentos:", error);
        throw error;
    }
}



