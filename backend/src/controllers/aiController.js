/**
 * 
 * @brief Divide un texto en fragmentos peque√±os, asegurando que cada uno no exceda un tama√±o m√°ximo y con superposici√≥n de contexto.
 * @param {string} text - El texto que se va a dividir en fragmentos.
 * @param {number} maxChunkSize - El tama√±o m√°ximo permitido para cada fragmento (por defecto 200 caracteres).
 * @param {number} overlapSize - El tama√±o de la superposici√≥n entre fragmentos (por defecto 20 caracteres).
 * 
 * @returns {Array<string>} Un array de fragmentos del texto, cada uno con un tama√±o m√°ximo de `maxChunkSize`.
 */
function splitTextIntoChunksWithOverlap(text, maxChunkSize = 200, overlapSize = 20) {
    // Divide el texto en oraciones utilizando expresiones regulares.
    const sentences = text.split(/(?<=\S[.!?])\s+/);

    const chunks = [];
    let currentChunk = "";

    sentences.forEach(sentence => {
        // Si agregar la oraci√≥n excede el tama√±o m√°ximo, comienza un nuevo fragmento y agrega superposici√≥n
        if ((currentChunk + sentence).length > maxChunkSize) {
            // Agrega superposici√≥n si es posible (superpone los √∫ltimos 20 caracteres)
            let overlap = currentChunk.slice(-overlapSize);
            chunks.push(currentChunk); // Agrega el fragmento anterior
            currentChunk = overlap + sentence; // Comienza el nuevo fragmento con la superposici√≥n
        } else {
            // De lo contrario, simplemente agrega la oraci√≥n al fragmento actual
            if (currentChunk) {
                currentChunk += " ";
            }
            currentChunk += sentence;
        }
    });

    // Agrega el √∫ltimo fragmento
    if (currentChunk.length > 0) {
        chunks.push(currentChunk);
    }

    return chunks;
}

/**
 * 
 * Funci√≥n para obtener los embeddings de un array de textos utilizando la API de OpenAI.
 * Si OpenAI no devuelve un embedding v√°lido, se inserta un vector de ceros de tama√±o 1536.
 * @param {Array<string>} texts - Array de textos a convertir en embeddings.
 * @returns {<Array<Array<number>>>} - Array de embeddings generados, 
 * cada uno representado como un vector de n√∫meros.
 */
async function getEmbeddings(texts) {
    const embeddings = [];

    // Itera sobre cada texto en el array de textos
    for (const text of texts) {
        try {
            // Llama a la API de OpenAI para obtener el embedding del texto
            const response = await openai.embeddings.create({
                model: "text-embedding-3-small",  // Modelo utilizado para obtener los embeddings
                input: text
            });

            console.log("‚úÖ Respuesta de OpenAI:", response.data[0].embedding);  // Muestra el embedding obtenido

            // Verifica si OpenAI ha devuelto una respuesta v√°lida, sino inserta un vector de ceros
            if (!response.data || response.data.length === 0) {
                console.error(`‚ùå OpenAI no devolvi√≥ embeddings para el texto: "${text}"`);
                embeddings.push(Array(1536).fill(0));
                continue;
            }

            // Agrega el embedding v√°lido al array de embeddings
            embeddings.push(response.data[0].embedding);

        } catch (error) {
            return console.error("‚ùå Error en la llamada a OpenAI:", error);
        }
    }

    return embeddings;
}




